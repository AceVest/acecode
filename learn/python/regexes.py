import re

# 引号内的特殊字符需要用 '\'　转义
regOne  = re.compile( "^((?:[(]\\d+[)])?\\s*\\d+(?:-\\d+)?)$")
# 以r开头，则引号内的字符按字面意思理解，不需要转义
regTwo  = re.compile(r"^((?:[(]\d+[)])?\s*\d+(?:-\d+)?)$")
